// profile_data.dart
class ProfileData {
  String firstName;
  String lastName;
  String studentCode;
  int semester;
  String faculty;
  String email;
  String avatarUrl;

  ProfileData({
    required this.firstName,
    required this.lastName,
    required this.studentCode,
    required this.semester,
    required this.faculty,
    required this.email,
    required this.avatarUrl,
  });
}
